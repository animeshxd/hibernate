package io.github.animeshxd;

public class Idd {

    private int id;
    private int nid;

    

    public Idd(int id, int nid) {
        this.id = id;
        this.nid = nid;
    }
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public int getNid() {
        return nid;
    }
    public void setNid(int nid) {
        this.nid = nid;
    }
    
}
